public class hw2_1 {
    public static void main(String[] args) {
        byte myDefaultByte = 0;
        short myDefaultShort = 0;
        int myDefaultInt = 0;
        long myDefaultLong = 0L;
        boolean myDefaultBoolean = false;
        double myDefaultDouble = 0.0d;
        float myDefaultFloat = 0.0f;
        char myDefaultChar = '\u0000';
        String myDefaultString = null;

        System.out.println("myDefaultByte = " + myDefaultByte);
        System.out.println("myDefaultShort = " + myDefaultShort);
        System.out.println("myDefaultInt = " + myDefaultInt);
        System.out.println("myDefaultLong = " + myDefaultLong);
        System.out.println("myDefaultBoolean = " + myDefaultBoolean);
        System.out.println("myDefaultDouble = " + myDefaultDouble);
        System.out.println("myDefaultFloat = " + myDefaultFloat);
        System.out.println("myDefaultChar = " + myDefaultChar);
        System.out.println("myDefaultString = " + myDefaultString);
    }
}
